package Test;

public class Datatype {

	public static void main(String[] args) {
		// int,double,float we will try to use all of them
		
		int a = 32;  // 4bytes = 32bits
		System.out.println(a);
		
		// Double
		
		double b = 2.34;
		System.out.println(b);
		
		// Float 
		
		Float t  = 2.33f;
		System.out.println(t);
		
		//Short
		
		Short s = 1; // 2bytes  -- 16 bites
		System.out.println(s);
		
		//byte
		
		Byte b1 = 1; // 1byte-- 8bits
		System.out.println(b1);
		
		//Characters
		
		char c = 'P'; //Char stores words
		System.out.println(c);
		
		//String
		
		String S = "Gand fad denge"; // Takes the whole sentence
		System.out.println(S);

	}

}
